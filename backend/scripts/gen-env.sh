#!/usr/bin/env sh
set -eu

SCRIPT_DIR=$(CDPATH= cd -- "$(dirname -- "$0")" && pwd)
BACKEND_DIR=$(CDPATH= cd -- "$SCRIPT_DIR/.." && pwd)
ENV_FILE="$BACKEND_DIR/.env"

if ! command -v openssl >/dev/null 2>&1; then
  echo "openssl is required to generate backend/.env" >&2
  exit 1
fi

if [ -f "$ENV_FILE" ] && [ "${FORCE:-}" != "1" ]; then
  echo "$ENV_FILE already exists. Set FORCE=1 to overwrite it." >&2
  exit 1
fi

rand_hex() {
  openssl rand -hex 64
}

JWT_ACCESS_SECRET=$(rand_hex)
JWT_REFRESH_SECRET=$(rand_hex)
LOGIN_CAPTCHA_SECRET=$(rand_hex)
QR_SIGNING_SECRET=$(rand_hex)
DB_PASSWORD=${DB_PASSWORD:-$(rand_hex)}
REDIS_PASSWORD=${REDIS_PASSWORD:-$(rand_hex)}

cat > "$ENV_FILE" <<EOF
